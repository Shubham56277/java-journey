package basics;

public class Arithmetic {
    public static void main(String[] args) {

        int a = 45;
        int b = 35;

        int sum = a + b;
        int diff = a - b;
        int mul = a * b;
        int div = a / b;

        System.out.println("Sum = " + sum);
        System.out.println("Difference = " + diff);
        System.out.println("Multiplication = " + mul);
        System.out.println("Division = " + div);
    }
}
